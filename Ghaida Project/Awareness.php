<li>نزيف اللثة بعد تنظيف الأسنان أو استخدام الخيط.</li>
<li>حساسية الأسنان.</li>
<li>صعوبة أو ألم أثناء المضغ.</li>
<li>سقوط الأسنان.</li>
</ul>

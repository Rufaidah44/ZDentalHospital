<p style="text-align: right; color:#2cbcbc;"><strong>تغيير فرشاة الأسنان:</strong><br /><p>ينصح بتغييرها كل ثلاثة أو أربعة أشهر، أو عند اهتراء شعيراتها وذلك للحفاظ على فعاليتها في التنظيف وعدم إيذاء أنسجة اللثة.</p>
<p style="text-align: right; color:#2cbcbc;"><strong>مشاكل الأسنان الشائعة أثناء الحمل:</strong><br /><p>توجد عدة مشاكل قد تزداد احتمالية حدوثها في فترة الحمل، وتعود إلى وضعها الطبيعي بعد الولادة، وتشمل:</p>
<ul style="text-align: right;">
<li><strong>التهاب اللثة:</strong><br />قد يحدث بسبب التغيرات الهرمونية التي تزيد استجابة أنسجة اللثة للبكتيريا.</li>
<li><strong>تسوس الأسنان:</strong><br /><p>تسوس الأسنان ليس مرضًا يحدث بسبب الحمل، وإنما تسببه البكتيريا وعادات الأكل الخاطئة، فقد يتغير النظام الغذائي في فترة الحمل (مثل زيادة تناول الوجبات الخفيفة)، أو تزداد حموضة الفم بسبب القيء، أو جفاف الفم، أو قلة نظافة الفم الناجمة عن الغثيان والقيء.</li>

<p style="text-align: right; color:#2cbcbc;"><strong>مقدمة :</strong><br /><p>الحمل هو فترة فريدة من حياة المرأة؛ حيث يحدث فيها العديد من التغيرات الجسدية مما قد يؤثر في صحة الفم، وفي الوقت نفسه تعد صحة الفم والأسنان هي المفتاح للصحة بشكل عام، حيث إن الوقاية والتشخيص والعلاج تعتبر ممارسات آمنة وفعّالة لتحسين صحة المرأة الحامل، بالإضافة إلى أنها مهمة لصحة الجنين أيضًا.</p>
